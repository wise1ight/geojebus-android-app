public class RouteStopItem {

    private String stopName;
    private String stopArs;
    private int stopId;
    private int stopNum;

    public String getStopName() {
        return stopName;
    }

    public void setStopName(String stopName) {
        this.stopName = stopName;
    }

    public String getStopArs() {
        return stopArs;
    }

    public void setStopArs(String stopArs) {
        this.stopArs = stopArs;
    }

    public int getStopId() {
        return stopId;
    }

    public void setStopId(int stopId) {
        this.stopId = stopId;
    }

    public int getStopNum() {
        return stopNum;
    }

    public void setStopNum(int stopNum) {
        this.stopNum = stopNum;
    }
}
